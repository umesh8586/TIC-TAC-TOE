    public void invokeButton(JButton btn)
    {
        if(!btn.getText().equals(""))
        {
            return;
        }
        total++;
        btn.setText(turn);
        if(turn.equals("O"))
        {
            btn.setForeground(magenta);
        }
        else
        {
            btn.setForeground(red);
        }
        checkWin();
        choosePlayer();

    }
