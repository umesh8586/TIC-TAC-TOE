    public void checkWin()
    {
        String bt1 = btn1.getText();
        String bt2 = btn2.getText();
        String bt3 = btn3.getText();
        String bt4 = btn4.getText();
        String bt5 = btn5.getText();
        String bt6 = btn6.getText();
        String bt7 = btn7.getText();
        String bt8 = btn8.getText();
        String bt9 = btn9.getText();
        
        if((bt1.equals(bt2) && bt2.equals(bt3)) && !bt1.equals(""))
        {
         setColorAndUpdateScore(btn1, btn2, btn3);   
        }
        else if((bt4.equals(bt5) && bt5.equals(bt6)) && !bt4.equals(""))
        {
         setColorAndUpdateScore(btn4, btn5, btn6);   
        } 
        else if((bt7.equals(bt8) && bt8.equals(bt9)) && !bt7.equals(""))
        {
         setColorAndUpdateScore(btn7, btn8, btn9);   
        }
        else if((bt1.equals(bt4) && bt4.equals(bt7)) && !bt1.equals(""))
        {
         setColorAndUpdateScore(btn1, btn4, btn7);   
        }
        else if((bt2.equals(bt5) && bt5.equals(bt8)) && !bt2.equals(""))
        {
         setColorAndUpdateScore(btn2, btn5, btn8);   
        }
        else if((bt3.equals(bt6) && bt6.equals(bt9)) && !bt3.equals(""))
        {
         setColorAndUpdateScore(btn3, btn6, btn9);   
        }
        else if((bt1.equals(bt5) && bt5.equals(bt9)) && !bt1.equals(""))
        {
         setColorAndUpdateScore(btn1, btn5, btn9);   
        }
        else if((bt3.equals(bt5) && bt5.equals(bt7)) && !bt3.equals(""))
        {
         setColorAndUpdateScore(btn3, btn5, btn7);   
        }
        else
        {
            if(total == 9)
     {
       JOptionPane.showMessageDialog(this, "Draw !", "Tic Toe Game", JOptionPane.INFORMATION_MESSAGE); 
       resetGame();
     }
        }
               
    }
    public void setColorAndUpdateScore(JButton btn1, JButton btn2, JButton btn3)
    {
     JOptionPane.showMessageDialog(this, "Player " + turn +" Wins", "Tic Toe Game", JOptionPane.INFORMATION_MESSAGE);
    
     btn1.setBackground(orange);
     btn2.setBackground(orange);
     btn3.setBackground(orange);
     
     if(turn.equals("X"))
     {
         xScore++;
     }
     else
     {
         oScore++;
     }
     updateScore();
     
     //resetGame();
    }
    public void updateScore()
    {
        xscore.setText(String.valueOf(xScore));
        oscore.setText(String.valueOf(oScore));
    }
